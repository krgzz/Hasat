    private void changePhoneButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_changePhoneButtonActionPerformed
        String phone = currentPhoneText.getText();
        String newPhone = newPhoneText.getText();
        String newPhone2 = newSecondPhoneText.getText();
        
        if (phone.isEmpty()) {
            currentPhoneText.setBorder(javax.swing.BorderFactory.createMatteBorder(1, 1, 1, 1, new java.awt.Color(250, 49, 74)));
            warningLabel.setBackground(new Color(99,16,1));
            warningLabel.setText("<html>&nbsp;● Mevcut Telefon kısmını<br>&nbsp;doldurmalısınız!</html>");
            return;
        }
        
        if (phone.length() != 10) {
            currentPhoneText.setBorder(javax.swing.BorderFactory.createMatteBorder(1, 1, 1, 1, new java.awt.Color(250, 49, 74)));
            warningLabel.setBackground(new Color(99,16,1));
            warningLabel.setText("<html>&nbsp;● Geçerli bir Telefon<br>&nbsp;girmelisiniz!</html>");
            return;
        }
        
        if (newPhone.isEmpty()) {
            newPhoneText.setBorder(javax.swing.BorderFactory.createMatteBorder(1, 1, 1, 1, new java.awt.Color(250, 49, 74)));
            warningLabel.setBackground(new Color(99,16,1));
            warningLabel.setText("<html>&nbsp;● Yeni Telefon kısmını<br>&nbsp;doldurmalısınız!</html>");
            return;
        }
        
        if (newPhone.length() != 10) {
            newPhoneText.setBorder(javax.swing.BorderFactory.createMatteBorder(1, 1, 1, 1, new java.awt.Color(250, 49, 74)));
            warningLabel.setBackground(new Color(99,16,1));
            warningLabel.setText("<html>&nbsp;● Geçerli bir yeni Telefon<br>&nbsp;girmelisiniz!</html>");
            return;
        }
        
        if (newPhone2.isEmpty()) {
            newSecondPhoneText.setBorder(javax.swing.BorderFactory.createMatteBorder(1, 1, 1, 1, new java.awt.Color(250, 49, 74)));
            warningLabel.setBackground(new Color(99,16,1));
            warningLabel.setText("<html>&nbsp;● Yeni Telefon kısmını<br>&nbsp;doldurmalısınız!</html>");
            return;
        }
        
        if (newPhone2.length() != 10) {
            newPhoneText.setBorder(javax.swing.BorderFactory.createMatteBorder(1, 1, 1, 1, new java.awt.Color(250, 49, 74)));
            warningLabel.setBackground(new Color(99,16,1));
            warningLabel.setText("<html>&nbsp;● Geçerli bir yeni Yelefon<br>&nbsp;girmelisiniz!</html>");
            return;
        }
        
        if (!newPhone.equals(newPhone2)) {
            newPhoneText.setBorder(javax.swing.BorderFactory.createMatteBorder(1, 1, 1, 1, new java.awt.Color(250, 49, 74)));
            newSecondPhoneText.setBorder(javax.swing.BorderFactory.createMatteBorder(1, 1, 1, 1, new java.awt.Color(250, 49, 74)));
            warningLabel.setBackground(new Color(99,16,1));
            warningLabel.setText("<html>&nbsp;● Yeni Telefon'lar<br>&nbsp;eşleşmiyor!</html>");
            return;
        }
       
        User user = UserManager.getUser();
        
        if (user == null) {
            dispose();
            
            new LoginFrame().setVisible(true);
            
            return;
        }
        
        if (!user.getPhone().equals(phone)) {
            currentPhoneText.setBorder(javax.swing.BorderFactory.createMatteBorder(1, 1, 1, 1, new java.awt.Color(250, 49, 74)));
            warningLabel.setBackground(new Color(99,16,1));
            warningLabel.setText("<html>&nbsp;● Mevcut Telefonunuz<br>&nbsp;yanlış!</html>");
            return;
        }
        
        MySQL.changePhone(user.getMail(), newPhone);
        
        user.setPhone(newPhone);
        
        warningLabel.setBackground(new Color(1,95,24));
        warningLabel.setText("<html>&nbsp;● Telefon numaranız<br>&nbsp;başarıyla değiştirildi!<br><br>&nbsp;&nbsp;Yönlendiriliyorsunuz...</html>");
        
        ActionListener action = (ActionEvent ae) -> {
            dispose();

            new ProfileFrame().setVisible(true);
        };

        Timer t = new Timer(3000, action);

        t.setRepeats(false);
        t.start();
    }//GEN-LAST:event_changePhoneButtonActionPerformed

