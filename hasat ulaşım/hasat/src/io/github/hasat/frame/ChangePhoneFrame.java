    private void currentPhoneTextKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_currentPhoneTextKeyTyped
        String key = String.valueOf(evt.getKeyChar());
        
        try {
            Integer.parseInt(key);
        } catch (NumberFormatException e) {
            evt.consume();
            return;
        }
        
        if (currentPhoneText.getText().length() > 9) {
            evt.consume();
            return;
        }
                
        currentPhoneText.setBorder(javax.swing.BorderFactory.createMatteBorder(1, 1, 1, 1, new java.awt.Color(255, 255, 255)));
    }//GEN-LAST:event_currentPhoneTextKeyTyped

